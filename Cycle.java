public class Cycle {
    public static void main(String[] args) {
        String[] quoutes={"love","peace","calm","growth","integrity"};
        System.out.println(quoutes.length);
        
        for (int i=0;i< quoutes.length;i++){
            System.out.println(quoutes[i]);
        }

        int i = 0;
        while (i < quoutes.length ) {
            System.out.println(quoutes[i]);
            i++;

            
        }
    }
    
}                                                                                                                  
